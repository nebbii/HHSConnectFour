    /**
     * Wipe playfield clear
     */
    public void wipePlayfield() {
        int x = 0;
        int y = 1;
        for (int i = 0; i < (6 * 7); i++) {
            if (x < 7) {
                x++;
            } else {
                x = 1;
                y++;
            }
            this.playfield[x][y] = 0;
        }
    }
    
